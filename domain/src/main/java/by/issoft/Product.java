package by.issoft;

public class Product {

    private final int id;
    private final String name;
    private final int rate;
    private final double price;

    public Product() {
        this.id = id;
        this.name = name;
        this.rate = rate;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getRate() {
        return rate;
    }

    public double getPrice() {
        return price;
    }
}
